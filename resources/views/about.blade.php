            <p style="text-align:justify">Following the success of this project, &ldquo;Sheikh Russel Digital Lab Establishment Project (Phase II)&rdquo; was adopted. Through this project, initiatives have been taken to establish 5000 Sheikh Russel Digital Lab and 300 Sheikh Russel Schools of Future out of 300 parliamentary seats each in each parliamentary constituency across the country. The process of establishing labs is well begun. The project has been approved at the ECNEC meeting on 26/08/2020. Expansion of ICT education from the grassroots level is considered a priority to address the challenges of the Fourth Industrial Revolution and to make Bangladesh a happy and prosperous country through the implementation of Vision 2041 announced by the Honourable Prime Minister. Over and above, training given to the students in coding and programming education from the primary and secondary level make them more creative and innovative to tackle the competition in the Fourth Industrial Revolution sector with their resourceful qualities. Moreover, the concept of the Sheikh Russel Digital Lab project will play a leading role in the sustainability and expansion of &#39;Digital Bangladesh&#39; activities with the aim of building a knowledge-based society, developing skilled human resources, and turning the country into a developed country by 2041.&nbsp;</p>

            <p style="text-align:justify"><span style="font-size:18px"><strong><span style="background-color:#dddddd">Concept of Sheikh Russel School of Future&nbsp;</span></strong></span></p>

            <p style="text-align:justify">There is no substitute for high-quality technological education. The &quot;School of Future&quot; is built on the foundation of pedagogical change in education, school architecture, critical thinking &amp; problem-solving skills and modern technology. To achieve this visionary goal, we need to go beyond the conventional classroom and integrate new technological improvements with hardware, digital content, software, School of Future Management Systems and new technologies (3D Printer, MR Tools, Robotics, Programmable etc) suitable for coping with the competition of 4th Industrial Revolution. The Sheikh Russel School of Future aims to equip our students with all classroom facilities such as creative &amp; constructive thinking, innovative skills and imparting knowledge and skills in the ICT sector through training in coding and programming education. The components of the Sheikh Russel School of Future have been selected based on these three concepts: education, architecture and technology. Students will be more interested to learn about the technologies of the 4th Industrial Revolution - especially the unique combination of new technologies like Nano, Cloud, lot, Robotics, Artificial Intelligence, Sensitive Drones, Blockchain as well as Electronics, Microchip and Robotics. They also get practical ideas about other technologies, including Lego sets, Arduino Starter Kit, Brick Pie Set, Mac Block Ultimate etc.&nbsp;</p>

